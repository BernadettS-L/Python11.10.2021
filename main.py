mood = input("How are you today?")

if mood == "happy":
    print("It is great to see you happy!")
elif mood == "nervous":
    print("Take a deep breath 3 times.")
elif mood == "sad":
    print("Gonna cry?")
elif mood == "excited":
    print("Good for you!")
elif mood == "relaxed":
    print("Finally you are chilling!")
else:
    print("I don't recognize this mood")
